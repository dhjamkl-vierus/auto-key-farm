/* ───────────────────  Context + Hook  ─────────────────── */
interface I18nValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}
const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ lang, setLang, children }: { lang: Lang; setLang: (l: Lang) => void; children: ReactNode; }) {
  const t = useCallback((key: string, vars?: Record<string, string | number>) => {
    const dict = STRINGS[lang] ?? en;
    const raw = dict[key] ?? en[key] ?? key;
    if (!vars) return raw;
    return raw.replace(/\{(\w+)\}/g, (_m, k) => String(vars[k] ?? ""));
  }, [lang]);

  // Apply <html lang> and dir for RTL languages
  useEffect(() => {
    const meta = LANGUAGES.find((l) => l.code === lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = meta?.rtl ? "rtl" : "ltr";
  }, [lang]);

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside <I18nProvider>");
  return ctx;
}

/** Lightweight standalone hook. */
export function useT() {
  return useI18n().t;
}

/** Default starting language. */
export const DEFAULT_LANG: Lang = "en";

/** Helper used by storage to coerce unknown language IDs. */
export function isLang(x: unknown): x is Lang {
  return typeof x === "string" && (x in STRINGS);
}

/** Internal: keep useState updater happy. */
export function _useLangState(initial: Lang) {
  return useState<Lang>(initial);
}
